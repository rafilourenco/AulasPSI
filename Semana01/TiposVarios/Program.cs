using System;

namespace TiposVarios
{
    class Program
    {
        static void Main(string[] args)
        {
            // Variavel Inteira
            int primeiro = 1;
            Console.WriteLine(primeiro);

            // Variavel Real
            double segundo = 1.55;
            Console.WriteLine(segundo);

            // Variavel Char
            char terceiro = 'A';
            Console.WriteLine(terceiro);
        }
    }
}
