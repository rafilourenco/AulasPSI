using System;
using System.Text;

namespace TiposVarios
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.OutputEncoding = Encoding.UTF8;
            // Variavel Inteira
            int primeiro = 1;
            Console.WriteLine(primeiro);

            // Variavel Real
            double segundo = 1.55;
            Console.WriteLine(segundo);

            // Variavel Char
            char terceiro = 'A';
            Console.WriteLine(terceiro);

            // Variavel Bool
            bool quarto = true;
            Console.WriteLine(quarto);
        }
    }
}
